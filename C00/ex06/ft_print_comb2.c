#include <unistd.h>

void	ft_print_comb2(void)
{
	int		n1;
	int		n2;
	char	c[5];

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			c[0] = n1 / 10 + '0';
			c[1] = n1 % 10 + '0';
			c[2] = ' ';
			c[3] = n2 / 10 + '0';
			c[4] = n2 % 10 + '0';
			write(1, c, 5);
			if (n1 != 98 || n2 != 99)
				write(1, ", ", 2);
			n2++;
		}
		n1++;
	}
	write(1, "\n", 1);
}
