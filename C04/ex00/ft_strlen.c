#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	int slen;

	slen = ft_strlen("test");
	printf("%d", slen);
}	
