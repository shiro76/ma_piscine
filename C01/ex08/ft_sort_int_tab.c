void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	sort;

	sort = 0;
	while (!sort)
	{
		sort = 1;
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i +1];
				tab[i + 1] = temp;
				sort = 0;
			}
			i++;
		}
	}
}
